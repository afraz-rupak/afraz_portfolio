/* =========================================================================
   store.js — lightweight content store (CMS layer)
   Loaded after data.js + blog-content.js, before site.js.

   The public site reads the in-memory data constants (PROFILE, PROJECTS, …).
   This file overlays any saved edits from localStorage onto those constants
   *in place* (const arrays/objects are mutable), so admin changes appear on
   the live site. The admin dashboard calls Store.save() to persist.
   ========================================================================= */

const Store = (function () {
  const KEY = "afraz-cms-v1";

  // Collections the dashboard can edit. Each maps a state key to its live const.
  function collections() {
    return {
      PROFILE: typeof PROFILE !== "undefined" ? PROFILE : null,
      STATS: typeof STATS !== "undefined" ? STATS : null,
      PROJECTS: typeof PROJECTS !== "undefined" ? PROJECTS : null,
      PUBLICATIONS: typeof PUBLICATIONS !== "undefined" ? PUBLICATIONS : null,
      BLOGS: typeof BLOGS !== "undefined" ? BLOGS : null,
      SKILLS: typeof SKILLS !== "undefined" ? SKILLS : null,
      RESEARCH_INTERESTS: typeof RESEARCH_INTERESTS !== "undefined" ? RESEARCH_INTERESTS : null,
      FOCUS_AREAS: typeof FOCUS_AREAS !== "undefined" ? FOCUS_AREAS : null,
    };
  }

  function setArr(target, items) {
    if (!Array.isArray(target) || !Array.isArray(items)) return;
    target.length = 0;
    items.forEach((i) => target.push(i));
  }

  function read() {
    try { return JSON.parse(localStorage.getItem(KEY)); } catch (e) { return null; }
  }

  // Overlay saved state onto the live constants (mutating in place).
  function hydrate() {
    const saved = read();
    if (!saved) return false;
    const c = collections();
    if (saved.PROFILE && c.PROFILE) {
      Object.keys(saved.PROFILE).forEach((k) => { c.PROFILE[k] = saved.PROFILE[k]; });
    }
    ["STATS", "PROJECTS", "PUBLICATIONS", "BLOGS", "SKILLS", "RESEARCH_INTERESTS", "FOCUS_AREAS"].forEach((k) => {
      if (saved[k] && c[k]) setArr(c[k], saved[k]);
    });
    return true;
  }

  // Snapshot the live constants into a plain object and persist.
  function save() {
    const c = collections();
    const out = {};
    Object.keys(c).forEach((k) => { if (c[k] != null) out[k] = c[k]; });
    try { localStorage.setItem(KEY, JSON.stringify(out)); return true; }
    catch (e) { return false; }
  }

  function reset() {
    try { localStorage.removeItem(KEY); } catch (e) {}
  }

  function hasOverrides() { return !!read(); }

  // Generate a paste-ready data.js fragment for the editable collections.
  function generateDataJs() {
    const c = collections();
    const blocks = [];
    const ser = (k) => JSON.stringify(c[k], null, 2);
    blocks.push("/* ---- Generated by the admin dashboard. Paste into assets/data.js ---- */\n");
    if (c.PROFILE) blocks.push("const PROFILE = " + ser("PROFILE") + ";\n");
    if (c.STATS) blocks.push("const STATS = " + ser("STATS") + ";\n");
    if (c.FOCUS_AREAS) blocks.push("const FOCUS_AREAS = " + ser("FOCUS_AREAS") + ";\n");
    if (c.PROJECTS) blocks.push("const PROJECTS = " + ser("PROJECTS") + ";\n");
    if (c.PUBLICATIONS) blocks.push("const PUBLICATIONS = " + ser("PUBLICATIONS") + ";\n");
    if (c.RESEARCH_INTERESTS) blocks.push("const RESEARCH_INTERESTS = " + ser("RESEARCH_INTERESTS") + ";\n");
    if (c.SKILLS) blocks.push("const SKILLS = " + ser("SKILLS") + ";\n");
    if (c.BLOGS) blocks.push("const BLOGS = " + ser("BLOGS") + ";\n");
    return blocks.join("\n");
  }

  // Utility: slugify a title into an id
  function slug(s) {
    return String(s).toLowerCase().trim()
      .replace(/[^a-z0-9\s-]/g, "")
      .replace(/\s+/g, "-")
      .replace(/-+/g, "-").slice(0, 60) || ("post-" + Date.now());
  }

  // hydrate immediately so the live site reflects saved edits
  hydrate();

  return { hydrate, save, reset, hasOverrides, generateDataJs, collections, slug, setArr, KEY };
})();
